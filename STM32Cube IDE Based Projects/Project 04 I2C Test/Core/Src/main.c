int main(void)
{

	HAL_StatusTypeDef ret;
	uint8_t buf[12];



  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();


  /* Configure the system clock */
  SystemClock_Config();


  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();

  while (1)
  {

	  uint8_t add= 0x88;
	  uint8_t cmd1 = 0x2C;
	  uint8_t cmd3 = 0x06;
	  buf[0] = cmd1;
	  buf[1]= cmd3;



	  HAL_I2C_Init(&hi2c1);
	  ret = HAL_I2C_Master_Transmit(&hi2c1, add, (uint8_t *) &buf, 2, HAL_MAX_DELAY);
	  HAL_Delay(300);
	  if(ret != HAL_OK)
	  	  {
	  		  while(1)
	  		  {
	  			  HAL_GPIO_TogglePin(LED_GPIO_Port, LED_Pin);
	  			  HAL_Delay(200);
	  		  }

	  	  }
	  HAL_I2C_Master_Receive(&hi2c1, add,(uint8_t *) &buf, 3, HAL_MAX_DELAY);

	    }
  HAL_Delay(5000);

}



